class Calculadora {
    sumar(num1, num2) {
        return num1 + num2;
    }

    restar(num1, num2) {
        return num1 - num2;
    }

    dividir(num1, num2) {
        return num1 / num2;
    }

    multiplicar(num1, num2) {
        return num1 * num2;
    }

    cuadrado(num) {
        return num * num;
    }
       
    raizCuadrada(num) {
        return Math.sqrt(num);
    }
       
    porcentaje(num) {
        return num / 100;
    }
       
} 

